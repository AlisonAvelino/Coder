public class Inferencia {

	public static void main(String[] args) {
		
		//Inferindo váriaveis dentro de métodos.
		
		
		//Declarando váriavel e iniciando com valor.
		double a = 4.5;
		System.out.println(a);
		
		//Atribuindo um valor inteiro.
		a = 12;
		System.out.println(a);
		
		//Inferindo com um double
		var b = 4.5;
		System.out.println(b);
		
		//Inferindo com uma string
		var c = "Texto";
		System.out.println(c);
		
		c =  "Outro texto";
		System.out.println(c);
		
	}
}
 